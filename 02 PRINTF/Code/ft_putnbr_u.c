#include "ft_printf.h"

void	ft_putnbr_u(unsigned int u, int *len)
{
	if (u < 10)
		ft_putchar(u + '0', len);
	else
	{
		ft_putnbr(u / 10, len);
		ft_putnbr(u % 10, len);
	}
}
